    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        String id = txtUsername.getText();
        String pass = txtPassword.getText();
        Boolean remember = chkRemember.isSelected();

        if (id.equals("") || pass.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đủ thông tin", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else if (id.equals("fpt") && pass.equals("polytechnic")) {
            JOptionPane.showMessageDialog(this, "Đăng nhập thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            if (remember) {
                JOptionPane.showMessageDialog(this, "Tài khoản của bạn đã được ghi nhớ!!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
            txtUsername.setText("");
            txtPassword.setText("");
            chkRemember.setSelected(false);
        } else {
            JOptionPane.showMessageDialog(this, "Đăng nhập thất bại\nVui lòng kiểm tra tài khoản và mật khẩu", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }

