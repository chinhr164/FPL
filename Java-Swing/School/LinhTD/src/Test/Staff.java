package Test;

public class Staff {

    private String id, name, gender, country, department;

    public Staff() {
    }

    public Staff(String id, String name, String gender, String country, String department) {
        this.id = id;
        this.name = name;
        this.gender = gender;
        this.country = country;
        this.department = department;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

}
